package Arrays;

public class cursos {
    public static void main(String[] args) {
        String[] cursos = {"Curso A", "Curso B", "Curso C"};
        double[] promedio = {4.5, 7.6, 9.3};
            
        for (int i = 0; i < cursos.length; i++) {
                System.out.println("El " + cursos[i] + " tiene un promedio de: " + promedio[i]);
        }
    }
}
        


